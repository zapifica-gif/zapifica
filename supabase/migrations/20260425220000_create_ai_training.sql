-- 4) Trigger updated_at (reusa utilitário se existir; cria se não existir)
create or replace function public.tg_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists ai_training_materials_touch_updated_at on public.ai_training_materials;
create trigger ai_training_materials_touch_updated_at
  before update on public.ai_training_materials
  for each row execute function public.tg_touch_updated_at();

create or replace function public.tg_touch_ai_company_context_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists ai_company_context_touch_updated_at on public.ai_company_context;
create trigger ai_company_context_touch_updated_at
  before update on public.ai_company_context
  for each row execute function public.tg_touch_ai_company_context_updated_at();

